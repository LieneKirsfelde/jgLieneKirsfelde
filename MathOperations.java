package lv.javaguru.lesson2;

public class MathOperations {
    public static void main(String[] args) {

        int firstNumber = 10;
        int seconNumber = 5;

        int sumResult = firstNumber + seconNumber;
        int multipyResult = firstNumber * seconNumber;
        int subtractResult = firstNumber - seconNumber;
        int divideRstult = firstNumber / seconNumber;

        System.out.println("Sum result: " + sumResult);
        System.out.println("Subtracting result: " + subtractResult);
        System.out.println("multiply result: " + multipyResult);
        System.out.println("divide result: " + divideRstult);
    }
}
